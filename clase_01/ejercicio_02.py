# Area de un triangulo.
base = float(input('Ingrese la base del triángulo: '))
altura = float(input('Ingrese la altura del triángulo: '))
area_triangulo = (base * altura) / 2
print('La area del triangulo es: ', area_triangulo)

# Perimetro de un cuadrado
lado = float(input('Ingrese la medida de uno de sus lados: '))
perimetro = lado * 4
print('El perimetro del cuadrado es:', perimetro)

# Volumen de una esfera
radio = float(input('Ingrese la medida del radio de la esfera: '))
pi = 3.14
volumen = (4 * pi * radio**3) / 3
print('El volumen de la esfera es:', volumen)
